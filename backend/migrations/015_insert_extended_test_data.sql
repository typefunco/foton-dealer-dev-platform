FROM performance 
GROUP BY year, quarter 
ORDER BY year, quarter;

SELECT 'after_sales' as table_name, year, quarter, COUNT(*) as records 
FROM after_sales 
GROUP BY year, quarter 
ORDER BY year, quarter;
